uint16_t ADC_Read_Channel(uint32_t channel)
{
    ADC_ChannelConfTypeDef sConfig = {0};

    sConfig.Channel = channel;
    sConfig.Rank = ADC_REGULAR_RANK_1;
    sConfig.SamplingTime = ADC_SAMPLETIME_239CYCLES_5;

    HAL_ADC_ConfigChannel(&hadc2, &sConfig);

    HAL_ADC_Start(&hadc2);

    HAL_ADC_PollForConversion(&hadc2, 10);

    return HAL_ADC_GetValue(&hadc2);
}
void ADC_Read_All(void)
{
    adc0 = ADC_Read_Channel(ADC_CHANNEL_0);
    adc1 = ADC_Read_Channel(ADC_CHANNEL_1);
    adc3 = ADC_Read_Channel(ADC_CHANNEL_3);

    lastVout = adc0 * 3.3f / 4095.0f * 13.0f * 13.0 / 18.6;
    lastVin  = adc1 * 3.3f / 4095.0f * 13.0f * 13.0 / 18.6;
		Vout = 0.3f * Vout + 0.7f * lastVout;
		Vin = 0.3f * Vin + 0.7f * lastVin;
    Pot = adc3;
}
void Read_Current_Filter(void)
{
//    float Current_new = ADC_Value[0] * 12.0f;
		float Current_new = ADC_Value[0] * 3.3f * 20.0f / 4095.0f / (0.008f * 40.0f);
		//float Current_new = ADC_Value[0] * 30.3f  / 4095.0f;
    Current = 0.3f * Current + 0.7f * Current_new;
}
