void PWM_Control_From_Pot(void)
{
    uint16_t pwm;

    // Low-pass filter
    pot_filter = 0.9f*pot_filter + 0.1f*(float)Pot;

    pwm = (uint16_t)(pot_filter * 999.0f / 4095.0f);

    TIM1->CCR1 = pwm;
		TIM1->CCR2 = 10;
}
